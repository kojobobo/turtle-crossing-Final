from turtle import Turtle


def create_b():

    turtle = Turtle()
    turtle.hideturtle()
    turtle.pensize(3)
    turtle.color('black')
    turtle.left(180)
    turtle.penup()
    turtle.goto(1610, -250)
    turtle.pendown()
    turtle.forward(3620)
    turtle.goto(-1620, 250)
    turtle.left(180)
    turtle.forward(3620)
